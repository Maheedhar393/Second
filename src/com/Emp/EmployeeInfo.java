package com.Emp;

public class EmployeeInfo {
	
	int empId;
	String empName;
	int payPerDay;
	int noOfdayWorked;
	int noOfHolidays;
	int spAllowance;
	int taxPercentage;
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public int getPayPerDay() {
		return payPerDay;
	}
	public void setPayPerDay(int payPerDay) {
		this.payPerDay = payPerDay;
	}
	public int getNoOfdayWorked() {
		return noOfdayWorked;
	}
	public void setNoOfdayWorked(int noOfdayWorked) {
		this.noOfdayWorked = noOfdayWorked;
	}
	public int getNoOfHolidays() {
		return noOfHolidays;
	}
	public void setNoOfHolidays(int noOfHolidays) {
		this.noOfHolidays = noOfHolidays;
	}
	public int getSpAllowance() {
		return spAllowance;
	}
	public void setSpAllowance(int spAllowance) {
		this.spAllowance = spAllowance;
	}
	public int getTaxPercentage() {
		return taxPercentage;
	}
	public void setTaxPercentage(int taxPercentage) {
		this.taxPercentage = taxPercentage;
	}
	
	
}
